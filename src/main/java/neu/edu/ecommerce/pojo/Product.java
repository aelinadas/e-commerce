package neu.edu.ecommerce.pojo;

/**
 *
 * @author aelinadas
 */
public class Product {
    private int id;
    private String productName;
    private double price;
    private String description;
    private String category;
    private int quantity;
    private String retailerName;
    private int numberOfProduct;
    private int numbberOfProductPerCustomer;
    private Retailer retailer;
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getNumberOfProduct() {
        return numberOfProduct;
    }

    public void setNumberOfProduct(int numberOfProduct) {
        this.numberOfProduct = numberOfProduct;
    }

    public int getNumbberOfProductPerCustomer() {
        return numbberOfProductPerCustomer;
    }

    public void setNumbberOfProductPerCustomer(int numbberOfProductPerCustomer) {
        this.numbberOfProductPerCustomer = numbberOfProductPerCustomer;
    }

    public String getRetailerName() {
        return retailerName;
    }

    public void setRetailerName(String retailerName) {
        this.retailerName = retailerName;
    }

    public Retailer getRetailer() {
        return retailer;
    }

    public void setRetailer(Retailer retailer) {
        this.retailer = retailer;
    }
}
